import numpy as np
from scipy.optimize import minimize
import matplotlib.pyplot as plt

# ============================================================
# ЭТАЛОННЫЕ ИНТЕГРАЛЫ ДЛЯ ЭЛЕКТРОНА (n=1)
# Взяты из вашего успешного BVP решения при α = 1/137
# ============================================================
I2_ref = 5.780647
I4_ref = 5.825971
I0_ref = 2.070358
alpha = 0.0072973525693

# ============================================================
# ФУНКЦИЯ ЭНЕРГИИ С УЧЁТОМ МАСШТАБИРОВАНИЯ И ЧИСЛА НАВИВКИ n
# ============================================================
def energy_scaled(params, n):
    """
    params: [lambda] - масштабный множитель координат (r -> r/lambda)
    n: число навивки фазы
    """
    λ = params[0]
    # Базовые члены (из масштабирования эталонного решения)
    E2 = I2_ref * λ
    E4 = I4_ref / λ
    E0 = 3 * alpha * I0_ref * λ**3

    # Центробежная энергия: пропорциональна n^2 и обратно пропорциональна моменту инерции.
    # Момент инерции тора ~ λ^5.
    # Константа C подобрана из условия dE/dλ = 0 при λ=1, n=1.
    C = (I2_ref - I4_ref + 9 * alpha * I0_ref) / 5
    E_centrifugal = C * n**2 / λ**5

    E_total = E2 + E4 + E0 + E_centrifugal
    return E_total

# ============================================================
# ПОИСК ОПТИМАЛЬНОГО МАСШТАБА ДЛЯ КАЖДОГО n
# ============================================================
n_values = [1, 6, 15]
results = {}

for n in n_values:
    # Минимизация по λ (начальное приближение 1.0)
    res = minimize(lambda x: energy_scaled(x, n), x0=[1.0], method='Nelder-Mead')
    λ_opt = res.x[0]
    E_opt = res.fun
    results[n] = {'λ': λ_opt, 'E': E_opt}
    print(f"n={n:2d}: λ_opt={λ_opt:.4f}, E={E_opt:.4f}")

# ============================================================
# СРАВНЕНИЕ С ПРЕДСКАЗАНИЕМ n^3
# ============================================================
E1 = results[1]['E']
print("\nСравнение отношений энергий с n^3:")
for n in n_values:
    ratio = results[n]['E'] / E1
    pred = n**3
    print(f"n={n:2d}: E/E1={ratio:.4f}, n^3={pred:.4f}, откл={abs(ratio-pred)/pred*100:.2f}%")

# ============================================================
# ГРАФИК ЭНЕРГИИ В ЗАВИСИМОСТИ ОТ n
# ============================================================
n_plot = np.array(n_values)
E_plot = np.array([results[n]['E'] for n in n_values])
plt.figure(figsize=(8,5))
plt.loglog(n_plot, E_plot, 'o-', label='Модель')
plt.loglog(n_plot, E1 * n_plot**3, 'r--', label='$n^3$')
plt.xlabel('Число навивки n')
plt.ylabel('Энергия')
plt.legend()
plt.grid(True, which='both', alpha=0.3)
plt.title('Зависимость энергии от числа навивки в адаптивной 1D модели')
plt.savefig('leptons_adaptive_1d.png', dpi=150)
plt.show()
